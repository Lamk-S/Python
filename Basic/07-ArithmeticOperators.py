"""
Los operadores aritméticos permiten realizar operaciones matemáticas 
entre variables o valores numéricos. A continuación se describen los 
principales operadores junto con sus ejemplos.
"""

# ------------------------------------------
# 1. Suma (+)
# ------------------------------------------
# Operador que permite adicionar dos valores.
# Ejemplo:
#   a + b
# Resultado: suma de ambos valores.

# ------------------------------------------
# 2. Resta (-)
# ------------------------------------------
# Permite restar un valor de otro.
# Ejemplo:
#   a - b

# ------------------------------------------
# 3. Multiplicación (*)
# ------------------------------------------
# Multiplica dos valores.
# Ejemplo:
#   a * b

# ------------------------------------------
# 4. División (/)
# ------------------------------------------
# Realiza una división normal y devuelve un resultado tipo float.
# Ejemplo:
#   a / b

# ------------------------------------------
# 5. División entera (//)
# ------------------------------------------
# Devuelve únicamente la parte entera del cociente.
# Ejemplo:
#   a // b

# ------------------------------------------
# 6. Módulo (%)
# ------------------------------------------
# Devuelve el residuo de una división.
# Ejemplo:
#   a % b

# ------------------------------------------
# 7. Exponente (**)
# ------------------------------------------
# Eleva un número a la potencia de otro.
# Ejemplo:
#   a ** b

# Pruebas
# a = 10
# b = 3
# print(a % b)
# print(a ** b)